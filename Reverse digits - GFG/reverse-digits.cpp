class Solution
{
	public:
		long long int reverse_digit(long long int n)
		{
		    // Code here
		    long long int m=0;
		    while(n!=0)
		    {
		    int r=n%10;
		    m=m*10+r;
		    n=n/10;
		    }
		    return m;
		}
};

